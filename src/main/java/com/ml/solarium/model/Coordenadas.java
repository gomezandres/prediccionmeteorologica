package com.ml.solarium.model;

public class Coordenadas {

	private double valorEjeX;
	private double valorEjeY;

	public Coordenadas() {
		super();
	}

	public Coordenadas(double valorEjeX, double valorEjeY) {
		super();
		this.valorEjeX = valorEjeX;
		this.valorEjeY = valorEjeY;
	}

	public double getValorEjeX() {
		return valorEjeX;
	}

	public void setValorEjeX(double valorEjeX) {
		this.valorEjeX = valorEjeX;
	}

	public double getValorEjeY() {
		return valorEjeY;
	}

	public void setValorEjeY(double valorEjeY) {
		this.valorEjeY = valorEjeY;
	}

}
